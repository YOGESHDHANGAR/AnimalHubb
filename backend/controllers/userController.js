// update User password
exports.updatePassword = catchAsyncErrors(async (req, res, next) => {
  const user = await User.findById(req.user.id).select("+password");

  const isPasswordMatched = await user.comparePassword(req.body.oldPassword);

  if (!isPasswordMatched) {
    return next(new ErrorHandler("Old password is incorrect", 400));
  }

  if (req.body.newPassword !== req.body.confirmPassword) {
    return next(new ErrorHandler("password does not match", 400));
  }

  user.password = req.body.newPassword;

  await user.save();

  sendToken(user, 200, res);
});

// update User Profile
exports.updateProfile = catchAsyncErrors(async (req, res, next) => {
  const newUserData = {};

  if (req.body.name) {
    newUserData.name = req.body.name;
  }

  if (req.body.longitude || req.body.latitude) {
    newUserData.longitude = req.body.longitude;
    newUserData.latitude = req.body.latitude;

    const lng = req.body.longitude;
    const lat = req.body.latitude;

    console.log("use location found:" + "lng:" + lng + "::" + "lat:" + lat);

    const { data } = await axios(
      `https://api.bigdatacloud.net/data/reverse-geocode-client?latitude=${lat}&longitude=${lng}&localityLanguage=en`
    );

    newUserData.place = data.localityInfo.administrative[2].name.split(" ")[0];
  }

  if (req.body.avatar) {
    const user = await User.findById(req.user._id);

    if (user.avatar.public_id) {
      const imageId = user.avatar.public_id;
      await cloudinary.v2.uploader.destroy(imageId);
    }

    const myCloud = await cloudinary.v2.uploader.upload(req.body.avatar, {
      folder: "avatars",
      width: 350,
      height: 250,
      crop: "scale",
    });

    newUserData.avatar = {
      public_id: myCloud.public_id,
      url: myCloud.secure_url,
    };
  }

  console.log("new use created:" + newUserData);

  const user = await User.findByIdAndUpdate(req.user._id, newUserData, {
    new: true,
    runValidators: true,
    useFindAndModify: false,
  });

  res.status(200).json({
    success: true,
  });
});
